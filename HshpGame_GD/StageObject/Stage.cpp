// プレイヤーとオブジェクトの当たり判定チェック
bool Stage::CollisionCheck(const Vec2 playerPos, int H, int W, ObjectType &object)
{
	if (m_stage[H][W] == 0) return false;

	float playerScale = 0.0f;
	if (m_pPlayer->GetPlayerState() == PlayerState::Wave) playerScale = kResizeScale;

	// 当たっている場合、trueを返す
	// ブロックの当たり判定
	if (m_ObjectBlock[H][W].GetRight() >= m_pPlayer->GetLeft() &&
		m_pPlayer->GetRight() >= m_ObjectBlock[H][W].GetLeft() &&
		m_ObjectBlock[H][W].GetBottom() >= m_pPlayer->GetTop() &&
		m_pPlayer->GetBottom() >= m_ObjectBlock[H][W].GetTop())
	{
		object = ObjectType::Block;
		return true;
	}
	// ジャンプリングの当たり判定
	if (m_ObjectJumpRing[H][W].GetRight() >= m_pPlayer->GetLeft() &&
		m_pPlayer->GetRight() >= m_ObjectJumpRing[H][W].GetLeft() &&
		m_ObjectJumpRing[H][W].GetBottom() >= m_pPlayer->GetTop() &&
		m_pPlayer->GetBottom() >= m_ObjectJumpRing[H][W].GetTop())
	{
		object = ObjectType::JumpRing;
		return true;
	}
	// ジャンプパッドの当たり判定
	if (m_ObjectJumpPad[H][W].GetRight() - 5 > m_pPlayer->GetLeft() &&
		m_pPlayer->GetRight() > m_ObjectJumpPad[H][W].GetLeft() + 5 &&
		m_ObjectJumpPad[H][W].GetBottom() > m_pPlayer->GetTop() &&
		m_pPlayer->GetBottom()> m_ObjectJumpPad[H][W].GetTop() + Game::kBlockSize - (Game::kBlockSize / 4))
	{
		object = ObjectType::JumpPad;
		return true;
	}
	// スパイクの当たり判定
	if (m_ObjectSpike[H][W].GetRight() - kResizeScale > m_pPlayer->GetLeft() + playerScale&&
		m_pPlayer->GetRight() - playerScale > m_ObjectSpike[H][W].GetLeft() + kResizeScale &&
		m_ObjectSpike[H][W].GetBottom() - kResizeScale > m_pPlayer->GetTop() + playerScale &&
		m_pPlayer->GetBottom() - playerScale > m_ObjectSpike[H][W].GetTop() + kResizeScale)
	{
		object = ObjectType::Spike;
		return true;
	}
	// グラビティリングの当たり判定
	if (m_ObjectGravityRing[H][W].GetRight() >= m_pPlayer->GetLeft() &&
		m_pPlayer->GetRight() >= m_ObjectGravityRing[H][W].GetLeft() &&
		m_ObjectGravityRing[H][W].GetBottom() >= m_pPlayer->GetTop() &&
		m_pPlayer->GetBottom() >= m_ObjectGravityRing[H][W].GetTop())
	{
		object = ObjectType::GravityRing;
		return true;
	}
	// ゴールゲートの判定
	if (m_ObjectGoalGate[H][W].GetRight() >= m_pPlayer->GetLeft() &&
		m_pPlayer->GetRight() >= m_ObjectGoalGate[H][W].GetLeft() &&
		m_ObjectGoalGate[H][W].GetBottom() >= m_pPlayer->GetTop() &&
		m_pPlayer->GetBottom() >= m_ObjectGoalGate[H][W].GetTop())
	{
		object = ObjectType::GoalGate;
		return true;
	}

	// 当たっていない場合、falseを返す
	return false;
}

// オブジェクトがプレイヤーの下にあるかどうか
bool Stage::IsUnder(const Vec2 playerPos, int H, int W, float& tempPos)
{	
	if (m_ObjectBlock[H][W].GetPos().y > m_pPlayer->GetCenterY())
	{
		// ブロックの座標を代入
		tempPos = m_ObjectBlock[H][W].GetPos().y - Game::kBlockSize;
		return true;
	}
	
	// 下ではない場合、falseを返す
	return false;
}

bool Stage::IsTop(const Vec2 playerPos, int H, int W, float& tempPos)
{
	if (m_ObjectBlock[H][W].GetPos().y + Game::kBlockSize < m_pPlayer->GetCenterY())
	{
		// ブロックの座標を代入
		tempPos = m_ObjectBlock[H][W].GetPos().y + Game::kBlockSize;
		return true;
	}

	// 上ではない場合、falseを返す
	return false;
}

void Stage::StageManage()
{
	if (m_stageState == StageState::firstStage || m_stageState == StageState::secondStage ||
		m_stageState == StageState::thirdStage || m_stageState == StageState::sixthStage ||
		m_stageState == StageState::eighthStage || m_stageState == StageState::ninthStage) 
		m_updateFunc = &Stage::NormalUpdate;
	else if (m_stageState == StageState::fourthStage || m_stageState == StageState::fifthStage ||
		m_stageState == StageState::seventhStage || m_stageState == StageState::tenthStage) 
		m_updateFunc = &Stage::ScrollUpdate;
	
	for (int i = 0; i < Game::kScreenHeightNum; i++)
	{
		for (int j = 0; j < Game::kScreenWidthTripleNum; j++)
		{
			if (m_stageState == StageState::firstStage)
			{
				m_stage[i][j] = stage_first[i][j];
			}
			else if (m_stageState == StageState::secondStage)
			{
				m_stage[i][j] = stage_second[i][j];
			}
			else if (m_stageState == StageState::thirdStage)
			{
				m_stage[i][j] = stage_third[i][j];
			}
			else if (m_stageState == StageState::fourthStage)
			{
				m_stage[i][j] = stage_fourth[i][j];
			}
			else if (m_stageState == StageState::fifthStage)
			{
				m_stage[i][j] = stage_fifth[i][j];
			}
			else if (m_stageState == StageState::sixthStage)
			{
				m_stage[i][j] = stage_sixth[i][j];
			}
			else if (m_stageState == StageState::seventhStage)
			{
				m_stage[i][j] = stage_seventh[i][j];
			}
			else if (m_stageState == StageState::eighthStage)
			{
				m_stage[i][j] = stage_eighth[i][j];
			}
			else if (m_stageState == StageState::ninthStage)
			{
				m_stage[i][j] = stage_ninth[i][j];
			}
			else if (m_stageState == StageState::tenthStage)
			{
				m_stage[i][j] = stage_tenth[i][j];
			}
			else if (m_stageState == StageState::debug)
			{
				m_stage[i][j] = stage_debug[i][j];
			}
			else
			{
				assert(0);
			}
		}
	}
}

void Stage::ChangeStageState()
{
	m_stageState = static_cast<StageState>(static_cast<int>(m_stageState) + 1);

	if (m_stageState == StageState::End) m_stageState = StageState::firstStage;
}

void Stage::NormalUpdate()
{
	if (m_pPlayer->IsDead()) return;

	for (int i = 0; i < Game::kScreenHeightNum; i++)
	{
		for (int j = 0; j < Game::kScreenWidthTripleNum; j++)
		{
			m_ObjectJumpRing[i][j].Update();
			m_ObjectGravityRing[i][j].Update();
		}
	}
}

void Stage::ScrollUpdate()
{
	SetStage();

	if (m_pPlayer->IsDead()) return;

	if (m_scroll > Game::kScreenWidthTriple - Game::kScreenWidth)
	{
		m_scroll = Game::kScreenWidthTriple - Game::kScreenWidth;
		m_scrollAcc = 0;
		m_pPlayer->SetPlayerVec(m_scroll);
	}
	else if (m_scroll < 0)
	{
		m_scroll = 0;
		m_scrollAcc = 0;
		m_pPlayer->SetPlayerVec(m_scroll);
	}

	if (m_pPlayer->GetRight() > Game::kScreenWidth || m_pPlayer->GetLeft() < 0)
	{
		m_canScroll = true;
	}

	if (m_canScroll)
	{
		if (m_pPlayer->GetCenterX() > Game::kScreenWidthHalf - (Game::kBlockSize / 2)
			&& m_pPlayer->GetCenterX() < Game::kScreenWidthHalf + (Game::kBlockSize / 2))
		{
			if (m_pPlayer->IsMoveRight()) m_scrollAcc = kScrollSpeed;
			else m_scrollAcc = -kScrollSpeed;
			m_pPlayer->DeleteVecX();
			m_canScroll = false;
		}
	}

	m_scroll += m_scrollAcc;

	for (int i = 0; i < Game::kScreenHeightNum; i++)
	{
		for (int j = 0; j < Game::kScreenWidthTripleNum; j++)
		{
			m_ObjectJumpRing[i][j].Update();
			m_ObjectGravityRing[i][j].Update();
		}
	}
}
